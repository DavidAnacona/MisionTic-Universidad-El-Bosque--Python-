"""
Haremos un algoritmo que nos e=muestre el area de un triangulo

para eso usaremos la formula de area de un trigualo = base * altura / 2

"""

print("Algoritmo para calcular el area de un triangulo")
#Entradas 
base = float(input("Digite la base del triangulo en cm "))
altura = float(input("Digite la altura del triangulo en cm "))

#procesos

area = 1/2 * base * altura

#Salidas

print("El area del triangulo con base de ",base," cm y una altura de ",altura," cm")
print("tiene una area de ",area," cm")

